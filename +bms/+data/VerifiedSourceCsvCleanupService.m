                'cache_path', '', 'cache_modified_millis', 0, ...
                'metadata_path', '', 'metadata_bytes', 0, ...
                'metadata_modified_millis', 0, 'cache_bytes', 0, ...
                'pair_id', '', 'mat_bytes', NaN, 'state', 'pending', ...
                'renamed_at', '', 'deleted_at', '', 'error_message', '');
        end
