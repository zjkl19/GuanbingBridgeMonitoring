        function assertCacheEntry(receipt, entry, cfg)
            cachePath = char(string(entry.cache_path));
            sourcePath = char(string(entry.source_path));
            [~, sourceBase] = fileparts(sourcePath);
            adapter = bms.data.JiulongjiangCsvDataSource.adapterFromConfig(cfg);
            expectedCacheDir = bms.data.JiulongjiangCsvDataSource.resolveCacheDir( ...
                char(string(receipt.csv_dir)), adapter);
            expectedCache = fullfile(expectedCacheDir, [sourceBase '.mat']);
            if ~strcmpi(bms.data.VerifiedSourceCsvCleanupService.canonicalPath( ...
                    cachePath), ...
                    bms.data.VerifiedSourceCsvCleanupService.canonicalPath( ...
                    expectedCache))
                error('BMS:CacheSourceCleanup:UnexpectedCachePath', ...
                    'Cache path is not the source CSV''s configured cache path: %s', ...
                    cachePath);
            end
            if ~bms.data.JiulongjiangCsvDataSource.validateStandaloneRawCache(cachePath, cfg)
                error('BMS:CacheSourceCleanup:CacheValidationFailed', ...
                    'Cache pair failed standalone validation: %s', cachePath);
            end
            meta = bms.io.JsonFile.read(entry.metadata_path);
            if ~isfield(meta, 'source_records')
                error('BMS:CacheSourceCleanup:CacheSourceIdentityMissing', ...
                    'Cache metadata has no source identity: %s', cachePath);
            end
            records = bms.data.CacheManager.normalizeSourceRecords(meta.source_records);
            if numel(records) ~= 1 ...
                    || ~strcmpi(bms.data.VerifiedSourceCsvCleanupService.canonicalPath( ...
                        records(1).path), ...
                        bms.data.VerifiedSourceCsvCleanupService.canonicalPath(sourcePath)) ...
                    || ~logical(records(1).exists) ...
                    || double(records(1).bytes) ~= double(entry.source_bytes) ...
                    || ~strcmp(char(string(records(1).modified_at)), ...
                        char(string(entry.source_modified_at)))
                error('BMS:CacheSourceCleanup:CacheSourceIdentityMismatch', ...
                    'Cache metadata is not bound to the source CSV receipt: %s', ...
                    cachePath);
            end
            payload = load(cachePath, 'meta');
            if ~isfield(payload, 'meta') || ~isstruct(payload.meta) ...
                    || ~isfield(payload.meta, 'src') ...
                    || ~strcmpi(bms.data.VerifiedSourceCsvCleanupService.canonicalPath( ...
                        payload.meta.src), ...
                        bms.data.VerifiedSourceCsvCleanupService.canonicalPath(sourcePath)) ...
                    || ~isfield(payload.meta, 'size') ...
                    || double(payload.meta.size) ~= double(entry.source_bytes) ...
                    || ~isfield(payload.meta, 'mtime') ...
                    || ~strcmp(datestr(double(payload.meta.mtime), ...
                        'yyyy-mm-dd HH:MM:ss'), ...
                        char(string(entry.source_modified_at)))
                error('BMS:CacheSourceCleanup:MatSourceIdentityMismatch', ...
                    'MAT cache is not bound to the source CSV receipt: %s', cachePath);
            end
            pairId = char(string(entry.pair_id));
            entryMatBytes = double(entry.mat_bytes);
            entryCacheBytes = double(entry.cache_bytes);
            cacheInfo = dir(cachePath);
            actualPairBytes = ...
                bms.data.JiulongjiangCsvDataSource.cachePairBytes(cachePath);
            if isempty(pairId) || ~isfinite(entryMatBytes) || entryMatBytes <= 0 ...
                    || ~isfinite(entryCacheBytes) || entryCacheBytes <= 0 ...
                    || isempty(cacheInfo) || double(cacheInfo(1).bytes) ~= entryMatBytes ...
                    || actualPairBytes ~= entryCacheBytes ...
                    || ~strcmp(bms.data.VerifiedSourceCsvCleanupService.fieldText(meta, 'pair_id'), ...
                        pairId) ...
                    || bms.data.VerifiedSourceCsvCleanupService.fieldDouble(meta, 'mat_bytes') ...
                        ~= entryMatBytes
                error('BMS:CacheSourceCleanup:CachePairChanged', ...
                    'Cache pair identity changed during cleanup: %s', cachePath);
            end
        end

        function assertReceiptBinding(receipt, cacheVersion, configHash, receiptPath, cfg)
            cleanupScope = bms.data.JiulongjiangCsvDataSource. ...
                configuredCleanupSourceScope(cfg);
            required = {'schema_version','receipt_type','receipt_id','status', ...
                'mode','commit_scope','recovery_policy','confirmation','confirmed_at', ...
                'day','partition_root','csv_dir','cache_version','config_hash', ...
                'cleanup_scope_hash','cleanup_scope_count','cleanup_scope_ids', ...
                'archive_proof','files'};
            receiptScopeIds = {};
            if isstruct(receipt) && isscalar(receipt) ...
                    && isfield(receipt, 'cleanup_scope_ids')
                receiptScopeIds = cellstr(lower(string(receipt.cleanup_scope_ids(:))));
                receiptScopeIds = sort(unique(receiptScopeIds));
            end
            if ~isstruct(receipt) || ~isscalar(receipt) || ~all(isfield(receipt, required)) ...
                    || double(receipt.schema_version) ~= 2 ...
                    || ~strcmp(char(string(receipt.receipt_type)), ...
                        'verified_cache_source_cleanup') ...
                    || ~strcmp(char(string(receipt.mode)), 'verified_extracted_csv') ...
                    || ~strcmp(char(string(receipt.commit_scope)), 'day') ...
                    || ~strcmp(char(string(receipt.recovery_policy)), 'verified_archive') ...
                    || ~strcmp(char(string(receipt.confirmation)), ...
                        'DELETE_VERIFIED_EXTRACTED_CSV') ...
                    || isempty(strtrim(char(string(receipt.confirmed_at)))) ...
                    || ~strcmp(char(string(receipt.cache_version)), char(string(cacheVersion))) ...
                    || ~strcmp(char(string(receipt.config_hash)), char(string(configHash))) ...
                    || ~strcmp(char(string(receipt.cleanup_scope_hash)), cleanupScope.hash) ...
                    || double(receipt.cleanup_scope_count) ~= cleanupScope.count ...
                    || numel(receiptScopeIds) ~= cleanupScope.count ...
                    || ~isequal(receiptScopeIds(:), cleanupScope.ids(:))
                error('BMS:CacheSourceCleanup:ReceiptBindingMismatch', ...
                    ['Cleanup receipt does not match the current cache/point scope ' ...
                     'contract: %s'], receiptPath);
            end
            canonicalReceipt = bms.data.VerifiedSourceCsvCleanupService.receiptPath( ...
                char(string(receipt.partition_root)));
            if ~strcmpi(bms.data.VerifiedSourceCsvCleanupService.canonicalPath( ...
                    canonicalReceipt), ...
                    bms.data.VerifiedSourceCsvCleanupService.canonicalPath(receiptPath)) ...
                    || ~bms.data.VerifiedSourceCsvCleanupService.isPathInside( ...
                        char(string(receipt.csv_dir)), ...
                        char(string(receipt.partition_root)))
                error('BMS:CacheSourceCleanup:ReceiptPathMismatch', ...
                    'Cleanup receipt paths are outside their declared partition: %s', receiptPath);
            end
            proofRequired = {'day','output_root','extraction_manifest_path','files'};
            proof = receipt.archive_proof;
            expectedManifest = fullfile(char(string(receipt.partition_root)), ...
                '.bms_extract_manifest.json');
            if ~isstruct(proof) || ~isscalar(proof) ...
                    || ~all(isfield(proof, proofRequired)) ...
                    || ~strcmp(char(string(proof.day)), char(string(receipt.day))) ...
                    || ~strcmpi(bms.data.VerifiedSourceCsvCleanupService.canonicalPath( ...
                        proof.output_root), ...
                        bms.data.VerifiedSourceCsvCleanupService.canonicalPath( ...
                        receipt.partition_root)) ...
                    || ~strcmpi(bms.data.VerifiedSourceCsvCleanupService.canonicalPath( ...
                        proof.extraction_manifest_path), ...
                        bms.data.VerifiedSourceCsvCleanupService.canonicalPath( ...
                        expectedManifest))
                error('BMS:CacheSourceCleanup:ReceiptArchiveBindingMismatch', ...
                    'Cleanup receipt recovery proof is not bound to its day/partition.');
            end
            for i = 1:numel(receipt.files)
                bms.data.VerifiedSourceCsvCleanupService.assertEntryPaths( ...
                    receipt, receipt.files(i));
            end
            proofFiles = receipt.archive_proof.files;
            proofSources = cellstr(string({proofFiles.path}));
            entrySources = cellstr(string({receipt.files.source_path}));
            normalizedEntries = unique(lower(string(entrySources)));
            normalizedProof = unique(lower(string(proofSources)));
            if numel(normalizedEntries) ~= numel(entrySources) ...
                    || numel(normalizedEntries) ~= numel(normalizedProof) ...
                    || ~all(ismember(normalizedEntries, normalizedProof)) ...
                    || ~all(ismember(normalizedProof, normalizedEntries))
                error('BMS:CacheSourceCleanup:ReceiptArchiveMismatch', ...
                    'Cleanup receipt entries are not a subset of its verified archive proof.');
            end
            for i = 1:numel(receipt.files)
                [~, sourceBase] = fileparts(char(string(receipt.files(i).source_path)));
                [~, cacheBase] = fileparts(char(string(receipt.files(i).cache_path)));
                proofIndex = find(strcmpi(proofSources, ...
                    char(string(receipt.files(i).source_path))), 1);
                if isempty(proofIndex) || ~strcmpi(sourceBase, cacheBase) ...
                        || double(proofFiles(proofIndex).bytes) ...
                            ~= double(receipt.files(i).source_bytes)
                    error('BMS:CacheSourceCleanup:ReceiptArchiveMismatch', ...
                        'Cleanup receipt source/cache/archive identity does not close.');
                end
            end
        end

