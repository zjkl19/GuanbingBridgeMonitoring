classdef VerifiedSourceCsvCleanupService
    %VERIFIEDSOURCECSVCLEANUPSERVICE Safely archive extracted CSV after caching.
    %   Destructive cleanup is task-scoped, disabled by default, and limited
    %   to verified daily-export partitions. Cache workers never delete source
    %   files; the parent process commits one day at a time and writes a
    %   durable receipt that supports cache-only discovery and crash recovery.

    methods (Static)
        function options = optionsFromTask(taskOptions)
            options = struct( ...
                'enabled', false, ...
                'mode', 'verified_extracted_csv', ...
                'commit_scope', 'day', ...
                'recovery_policy', 'verified_archive', ...
                'confirmation', '', ...
                'confirmed_at', '');
            if nargin < 1 || isempty(taskOptions), return; end
            if ~isstruct(taskOptions) || ~isscalar(taskOptions)
                error('BMS:CacheSourceCleanup:InvalidTaskOptions', ...
                    'Task options must be a scalar struct.');
            end
            if ~isfield(taskOptions, 'cache_source_cleanup') ...
                    || isempty(taskOptions.cache_source_cleanup)
                return;
            end
            section = taskOptions.cache_source_cleanup;
            if ~isstruct(section) || ~isscalar(section)
                error('BMS:CacheSourceCleanup:InvalidOptions', ...
                    'options.cache_source_cleanup must be a scalar object.');
            end
            allowed = fieldnames(options);
            unknown = setdiff(fieldnames(section), allowed, 'stable');
            if ~isempty(unknown)
                error('BMS:CacheSourceCleanup:UnknownOption', ...
                    'Unknown cache_source_cleanup option(s): %s', strjoin(unknown, ', '));
            end
            for i = 1:numel(allowed)
                name = allowed{i};
                if isfield(section, name) && ~isempty(section.(name))
                    options.(name) = section.(name);
                end
            end
            if ~(islogical(options.enabled) || isnumeric(options.enabled)) ...
                    || ~isscalar(options.enabled) || ~isfinite(double(options.enabled)) ...
                    || ~ismember(double(options.enabled), [0 1])
                error('BMS:CacheSourceCleanup:InvalidEnabledFlag', ...
                    'cache_source_cleanup.enabled must be one boolean value.');
            end
            options.enabled = logical(options.enabled);
            options.mode = lower(strtrim(char(string(options.mode))));
            options.commit_scope = lower(strtrim(char(string(options.commit_scope))));
            options.recovery_policy = lower(strtrim(char(string(options.recovery_policy))));
            options.confirmation = char(string(options.confirmation));
            options.confirmed_at = char(string(options.confirmed_at));
            if ~options.enabled, return; end
            if ~strcmp(options.mode, 'verified_extracted_csv') ...
                    || ~strcmp(options.commit_scope, 'day') ...
                    || ~strcmp(options.recovery_policy, 'verified_archive')
                error('BMS:CacheSourceCleanup:UnsafePolicy', ...
                    ['CSV cleanup requires mode=verified_extracted_csv, ' ...
                     'commit_scope=day and recovery_policy=verified_archive.']);
            end
            if ~strcmp(options.confirmation, 'DELETE_VERIFIED_EXTRACTED_CSV')
                error('BMS:CacheSourceCleanup:ConfirmationRequired', ...
                    ['Destructive CSV cleanup was requested without the exact ' ...
                     'DELETE_VERIFIED_EXTRACTED_CSV confirmation token.']);
            end
            if isempty(strtrim(options.confirmed_at))
                error('BMS:CacheSourceCleanup:ConfirmationTimeRequired', ...
                    'Destructive CSV cleanup requires a non-empty confirmation timestamp.');
            end
            enabledModules = bms.module.ModuleRegistry.enabledFromOptions(taskOptions);
            allowedModules = {'zip_precheck','unzip','cache_prebuild'};
            unsafe = enabledModules(~ismember({enabledModules.Key}, allowedModules));
            if ~isempty(unsafe)
                error('BMS:CacheSourceCleanup:DedicatedTaskRequired', ...
                    ['Verified CSV cleanup must run as a dedicated preprocessing task. ' ...
                     'Remove these later/mutating modules and run analysis from MAT in a ' ...
                     'separate task: %s'], strjoin({unsafe.Label}, ', '));
            end
        end

        function tf = isEnabled(taskOptions)
            tf = bms.data.VerifiedSourceCsvCleanupService.optionsFromTask(taskOptions).enabled;
        end

        function path = receiptPath(partitionRoot)
            path = fullfile(char(string(partitionRoot)), ...
                '.bms_cache_source_cleanup_receipt.json');
        end

        function root = partitionRoot(csvDir)
            root = char(string(csvDir));
            for i = 1:12
                if isfile(fullfile(root, '.bms_extract_manifest.json'))
                    return;
                end
                parent = fileparts(root);
                if isempty(parent) || strcmp(parent, root), break; end
                root = parent;
            end
            error('BMS:CacheSourceCleanup:ExtractionManifestMissing', ...
                'No verified daily extraction manifest is an ancestor of: %s', csvDir);
        end

        function resumePending(csvDirs, cfg, taskOptions, cacheVersion, configHash)
            if nargin < 3, taskOptions = struct(); end
            if nargin < 4, cacheVersion = 'jlj_csv_v2'; end
            if nargin < 5, configHash = ''; end
            csvDirs = unique(cellstr(string(csvDirs)), 'stable');
            roots = {};
            for i = 1:numel(csvDirs)
                try
                    roots{end+1} = bms.data.VerifiedSourceCsvCleanupService.partitionRoot( ...
                        csvDirs{i}); %#ok<AGROW>
                catch
                    orphanReceipt = ...
                        bms.data.VerifiedSourceCsvCleanupService.findReceiptAncestor( ...
                        csvDirs{i});
                    if ~isempty(orphanReceipt)
                        error('BMS:CacheSourceCleanup:ExtractionManifestLost', ...
                            ['A cleanup receipt exists but its verified extraction manifest ' ...
                             'is missing: %s'], orphanReceipt);
                    end
                end
            end
            roots = unique(roots, 'stable');
            for i = 1:numel(roots)
                receiptPath = bms.data.VerifiedSourceCsvCleanupService.receiptPath(roots{i});
                if ~isfile(receiptPath), continue; end
                snapshot = bms.io.JsonFile.read(receiptPath);
                if strcmp(char(string(snapshot.status)), 'committed'), continue; end
                options = bms.data.VerifiedSourceCsvCleanupService.optionsFromTask(taskOptions);
                if ~options.enabled
                    error('BMS:CacheSourceCleanup:ResumeConfirmationRequired', ...
                        ['An interrupted destructive cleanup exists at %s. Re-run with ' ...
                         'the explicit cleanup confirmation to reconcile it.'], receiptPath);
                end
                if ~isfield(snapshot, 'day') ...
                        || isempty(strtrim(char(string(snapshot.day))))
                    error('BMS:CacheSourceCleanup:ReceiptBindingMismatch', ...
                        'Interrupted cleanup receipt has no natural day: %s', ...
                        receiptPath);
                end
                outputRoot = fileparts( ...
                    bms.data.VerifiedSourceCsvCleanupService. ...
                        canonicalPath(roots{i}));
                % Public/direct resume callers must own the same day mutation
                % lock as extraction and cache publication. Preserve the
                % global order day -> partition even when an outer cache run
                % already holds this re-entrant day lease.
                dayLease = bms.data.DailyExportMutationLock.acquire( ...
                    outputRoot, snapshot.day); %#ok<NASGU>
                partitionLease = ...
                    bms.data.VerifiedSourceCsvCleanupService. ...
                        acquirePartitionLease(roots{i}); %#ok<NASGU>
                if ~isfile(receiptPath)
                    error('BMS:CacheSourceCleanup:ReceiptChangedUnderLock', ...
                        'Interrupted cleanup receipt disappeared: %s', receiptPath);
                end
                receipt = bms.io.JsonFile.read(receiptPath);
                if ~strcmp(char(string(receipt.day)), ...
                        char(string(snapshot.day)))
                    error('BMS:CacheSourceCleanup:ReceiptChangedUnderLock', ...
                        'Cleanup receipt day changed while acquiring its locks.');
                end
                if strcmp(char(string(receipt.status)), 'committed'), continue; end
                bms.data.VerifiedSourceCsvCleanupService.resumeReceipt( ...
                    receiptPath, receipt, cfg, cacheVersion, configHash);
            end
        end

        function verification = reconcilePendingDay(root, day, cfg, taskOptions)
            %RECONCILEPENDINGDAY Finish an interrupted daily cleanup before unzip.
            %   This deliberately keeps mutation out of verifyCommittedDay.
            %   A daily streaming restart first reconciles a prepared/partial
            %   receipt under the original explicit task confirmation, then
            %   proves the resulting committed day can safely skip extraction.
            if nargin < 3, cfg = struct(); end
            if nargin < 4, taskOptions = struct(); end
            dayText = bms.data.TimeRangeResolver.toDateString(day);
            csvDirs = bms.data.ZipDailyExportAdapter.csvDirs( ...
                root, dayText, dayText, cfg);
            adapter = bms.data.JiulongjiangCsvDataSource.adapterFromConfig(cfg);
            cacheVersion = 'jlj_csv_v2';
            configHash = bms.data.CacheManager.configHash(adapter);
            bms.data.VerifiedSourceCsvCleanupService.resumePending( ...
                csvDirs, cfg, taskOptions, cacheVersion, configHash);
            verification = bms.data.VerifiedSourceCsvCleanupService. ...
                verifyCommittedDay(root, dayText, cfg);
        end

        function discovery = archivedDiscovery(csvDirs, cfg, cacheVersion, configHash)
            %ARCHIVEDDISCOVERY Enumerate only caches authorised by receipts.
            if nargin < 3, cacheVersion = 'jlj_csv_v2'; end
            if nargin < 4, configHash = ''; end
            rows = {};
            csvDirs = unique(cellstr(string(csvDirs)), 'stable');
            for i = 1:numel(csvDirs)
                try
                    partitionRoot = bms.data.VerifiedSourceCsvCleanupService.partitionRoot( ...
                        csvDirs{i});
                catch
                    orphanReceipt = ...
                        bms.data.VerifiedSourceCsvCleanupService.findReceiptAncestor( ...
                        csvDirs{i});
                    if ~isempty(orphanReceipt)
                        error('BMS:CacheSourceCleanup:ExtractionManifestLost', ...
                            ['Archived cache discovery found a cleanup receipt without its ' ...
                             'verified extraction manifest: %s'], orphanReceipt);
                    end
                    continue;
                end
                receiptPath = bms.data.VerifiedSourceCsvCleanupService.receiptPath(partitionRoot);
                if ~isfile(receiptPath), continue; end
                receipt = bms.io.JsonFile.read(receiptPath);
                bms.data.VerifiedSourceCsvCleanupService.assertReceiptBinding( ...
                    receipt, cacheVersion, configHash, receiptPath, cfg);
                if bms.data.VerifiedSourceCsvCleanupService. ...
                        hasCleanupAuthorization(receipt)
                    bms.data.VerifiedSourceCsvCleanupService. ...
                        assertCleanupAuthorization(receipt, receiptPath, ...
                            bms.data.VerifiedSourceCsvCleanupService. ...
                                expectedJljCleanupLockPath(receipt), false);
                end
                if ~strcmp(char(string(receipt.status)), 'committed')
                    error('BMS:CacheSourceCleanup:ReceiptIncomplete', ...
                        'Cleanup receipt is not committed: %s', receiptPath);
                end
                bms.data.VerifiedSourceCsvCleanupService.assertCommittedReceiptState( ...
                    receipt, receiptPath);
                if ~bms.data.ArchiveExtractService.verifyArchiveProof(receipt.archive_proof)
                    error('BMS:CacheSourceCleanup:RecoveryArchiveChanged', ...
                        'Recovery ZIP changed after CSV cleanup: %s', receiptPath);
                end
                entries = receipt.files;
                if isempty(entries), continue; end
                for j = 1:numel(entries)
                    bms.data.VerifiedSourceCsvCleanupService.assertCacheEntry( ...
                        receipt, entries(j), cfg);
                    source = char(string(entries(j).source_path));
                    temp = char(string(entries(j).temporary_path));
                    if isfile(source)
                        continue;
                    end
                    if isfile(temp)
                        error('BMS:CacheSourceCleanup:TemporarySourceRemains', ...
                            'Pending-delete CSV remains after a committed receipt: %s', temp);
                    end
                    cachePath = char(string(entries(j).cache_path));
                    if ~bms.data.JiulongjiangCsvDataSource.validateStandaloneRawCache( ...
                            cachePath, cfg)
                        error('BMS:CacheSourceCleanup:ArchivedCacheInvalid', ...
                            'Archived CSV cache is no longer independently valid: %s', cachePath);
                    end
                    rows{end+1} = struct( ... %#ok<AGROW>
                        'path', source, ...
                        'source_bytes', double(entries(j).source_bytes), ...
                        'source_modified_at', char(string(entries(j).source_modified_at)), ...
                        'header', {{}}, ...
                        'classification', 'eligible', ...
                        'reason', 'verified_source_archived', ...
                        'error_identifier', '', ...
                        'error_message', '', ...
                        'source_present', false, ...
                        'cache_path', cachePath, ...
                        'metadata_path', char(string(entries(j).metadata_path)), ...
                        'cleanup_receipt_path', receiptPath, ...
                        'csv_dir', char(string(csvDirs{i})), ...
                        'partition_root', partitionRoot);
                end
            end
            if isempty(rows), discovery = struct([]); else, discovery = [rows{:}]; end
        end

        function verification = verifyCommittedDay(root, day, cfg)
            %VERIFYCOMMITTEDDAY Prove one previously cleaned day is reusable.
            %   A new streaming session must not attempt to re-extract a day
            %   whose eligible CSV files were deliberately removed.  This
            %   method only authorises that skip after the receipt, recovery
            %   ZIP, cache pairs and the absent source/staging state all close.
            if nargin < 3, cfg = struct(); end
            dayText = bms.data.TimeRangeResolver.toDateString(day);
            verification = struct('committed', false, 'day', dayText, ...
                'receipt_path', '', 'cache_count', 0, ...
                'deleted_count', 0, 'deleted_bytes', 0);
            csvDirs = bms.data.ZipDailyExportAdapter.csvDirs( ...
                root, dayText, dayText, cfg);
            if isempty(csvDirs), return; end
            adapter = bms.data.JiulongjiangCsvDataSource.adapterFromConfig(cfg);
            cacheVersion = 'jlj_csv_v2';
            configHash = bms.data.CacheManager.configHash(adapter);
            matched = {};
            for i = 1:numel(csvDirs)
                partitionRoot = ...
                    bms.data.VerifiedSourceCsvCleanupService.partitionRoot(csvDirs{i});
                receiptPath = ...
                    bms.data.VerifiedSourceCsvCleanupService.receiptPath(partitionRoot);
                if isfile(receiptPath)
                    matched{end+1} = struct('csv_dir', csvDirs{i}, ... %#ok<AGROW>
                        'partition_root', partitionRoot, ...
                        'receipt_path', receiptPath);
                end
            end
            if isempty(matched), return; end
            if numel(matched) ~= 1
                error('BMS:CacheSourceCleanup:CommittedDayAmbiguous', ...
                    'Expected exactly one cleanup receipt for %s.', dayText);
            end
            match = matched{1};
            receipt = bms.io.JsonFile.read(match.receipt_path);
            bms.data.VerifiedSourceCsvCleanupService.assertReceiptBinding( ...
                receipt, cacheVersion, configHash, match.receipt_path, cfg);
            if bms.data.VerifiedSourceCsvCleanupService. ...
                    hasCleanupAuthorization(receipt)
                bms.data.VerifiedSourceCsvCleanupService. ...
                    assertCleanupAuthorization(receipt, match.receipt_path, ...
                        bms.data.VerifiedSourceCsvCleanupService. ...
                            expectedJljCleanupLockPath(receipt), false);
            end
            if ~strcmp(char(string(receipt.status)), 'committed')
                error('BMS:CacheSourceCleanup:ReceiptIncomplete', ...
                    'Cleanup receipt is not committed: %s', match.receipt_path);
            end
            bms.data.VerifiedSourceCsvCleanupService.assertCommittedReceiptState( ...
                receipt, match.receipt_path);
            if ~strcmp(char(string(receipt.day)), dayText) ...
                    || ~bms.data.VerifiedSourceCsvCleanupService.isPathInside( ...
                        match.partition_root, root)
                error('BMS:CacheSourceCleanup:ReceiptDayMismatch', ...
                    'Cleanup receipt is not bound to requested day/root: %s', ...
                    match.receipt_path);
            end
            if ~bms.data.ArchiveExtractService.verifyArchiveProof(receipt.archive_proof)
                error('BMS:CacheSourceCleanup:RecoveryArchiveChanged', ...
                    'Recovery ZIP changed after CSV cleanup: %s', match.receipt_path);
            end
            entries = receipt.files;
            if isempty(entries)
                error('BMS:CacheSourceCleanup:ReceiptEmpty', ...
                    'Committed cleanup receipt has no cache entries: %s', ...
                    match.receipt_path);
            end
            for i = 1:numel(entries)
                source = char(string(entries(i).source_path));
                temp = char(string(entries(i).temporary_path));
                if isfile(source) || isfile(temp)
                    error('BMS:CacheSourceCleanup:CommittedSourceStateInvalid', ...
                        ['Committed cleanup receipt still has a source or staged CSV; ' ...
                         'refusing to skip extraction: %s'], source);
                end
                bms.data.VerifiedSourceCsvCleanupService.assertCacheEntry( ...
                    receipt, entries(i), cfg);
            end
            verification.committed = true;
            verification.receipt_path = match.receipt_path;
            verification.cache_count = numel(entries);
            verification.deleted_count = double(receipt.deleted_count);
            verification.deleted_bytes = double(receipt.deleted_bytes);
        end

        function result = commitJljPartition(root, day, csvDir, records, cfg, ...
                taskOptions, cacheVersion, configHash)
            options = bms.data.VerifiedSourceCsvCleanupService.optionsFromTask(taskOptions);
            result = struct('enabled', options.enabled, 'status', 'retained', ...
                'day', char(string(day)), 'receipt_path', '', ...
                'deleted_count', 0, 'deleted_bytes', 0);
            if ~options.enabled, return; end
            if nargin < 7 || isempty(cacheVersion), cacheVersion = 'jlj_csv_v2'; end
            if nargin < 8, configHash = ''; end
            if isempty(records)
                error('BMS:CacheSourceCleanup:NoEligibleRecords', ...
                    'No eligible cache records exist for %s.', char(string(day)));
            end
            % Keep the public commit API fail-safe as well as its current
            % callers.  The common day mutation lock serialises extraction,
            % cache generation and cleanup; acquisition precedes the narrower
            % partition lease to preserve the global lock order.
            dayMutationLease = bms.data.DailyExportMutationLock.acquire( ...
                root, char(string(day))); %#ok<NASGU>
            cleanupLockPath = bms.data.DailyExportMutationLock.pathFor( ...
                root, char(string(day)));
            partitionRoot = bms.data.VerifiedSourceCsvCleanupService.partitionRoot(csvDir);
            partitionOutputRoot = fileparts( ...
                bms.data.VerifiedSourceCsvCleanupService. ...
                    canonicalPath(partitionRoot));
            expectedCleanupLockPath = ...
                bms.data.DailyExportMutationLock.pathFor( ...
                    partitionOutputRoot, char(string(day)));
            if ~strcmpi( ...
                    bms.data.VerifiedSourceCsvCleanupService. ...
                        canonicalPath(cleanupLockPath), ...
                    bms.data.VerifiedSourceCsvCleanupService. ...
                        canonicalPath(expectedCleanupLockPath))
                error('BMS:CacheSourceCleanup:AuthorizationLockMismatch', ...
                    ['Daily-export partition and cache root resolve to ' ...
                     'different mutation locks.']);
            end
            partitionLease = bms.data.VerifiedSourceCsvCleanupService. ...
                acquirePartitionLease(partitionRoot); %#ok<NASGU>
            receiptPath = bms.data.VerifiedSourceCsvCleanupService.receiptPath(partitionRoot);
            if isfile(receiptPath)
                    previous = bms.io.JsonFile.read(receiptPath);
                if strcmp(char(string(previous.status)), 'committed')
                    bms.data.VerifiedSourceCsvCleanupService.assertReceiptBinding( ...
                        previous, cacheVersion, configHash, receiptPath, cfg);
                    if bms.data.VerifiedSourceCsvCleanupService. ...
                            hasCleanupAuthorization(previous)
                        bms.data.VerifiedSourceCsvCleanupService. ...
                            assertCleanupAuthorization(previous, receiptPath, ...
                                bms.data.VerifiedSourceCsvCleanupService. ...
                                    expectedJljCleanupLockPath(previous), false);
                    end
                    bms.data.VerifiedSourceCsvCleanupService.assertCommittedReceiptState( ...
                        previous, receiptPath);
                    if ~bms.data.ArchiveExtractService.verifyArchiveProof( ...
                            previous.archive_proof)
                        error('BMS:CacheSourceCleanup:RecoveryArchiveChanged', ...
                            'Recovery ZIP changed after the existing cleanup: %s', receiptPath);
                    end
                    currentSources = cellstr(string({records.source_path}));
                    previousSources = cellstr(string({previous.files.source_path}));
                    if ~all(ismember(lower(string(currentSources)), ...
                            lower(string(previousSources))))
                        error('BMS:CacheSourceCleanup:CommittedReceiptExtensionForbidden', ...
                            ['A committed daily receipt cannot be overwritten with new source ' ...
                             'paths. Restore/re-extract and review the whole day explicitly.']);
                    end
                    if any(cellfun(@isfile, currentSources))
                        previous = bms.data.VerifiedSourceCsvCleanupService.commitReceipt( ...
                            receiptPath, previous, cfg);
                        result.status = previous.status;
                        result.receipt_path = receiptPath;
                        result.deleted_count = previous.deleted_count;
                        result.deleted_bytes = previous.deleted_bytes;
                    else
                        result.status = 'already_archived';
                        result.receipt_path = receiptPath;
                    end
                    return;
                else
                    bms.data.VerifiedSourceCsvCleanupService.resumeReceipt( ...
                        receiptPath, previous, cfg, cacheVersion, configHash);
                end
            end

            statuses = cellstr(string({records.status}));
            failed = strcmp(statuses, 'failed');
            if any(failed)
                error('BMS:CacheSourceCleanup:PartitionCacheFailed', ...
                    'At least one eligible cache failed for %s; no CSV was deleted.', ...
                    char(string(day)));
            end
            present = arrayfun(@(rec) isfield(rec, 'source_present') ...
                && logical(rec.source_present) && isfile(char(string(rec.source_path))), records);
            records = records(present);
            if isempty(records)
                result.status = 'already_archived';
                result.receipt_path = receiptPath;
                return;
            end
            validStatus = ismember(cellstr(string({records.status})), ...
                {'created','reused','rebuilt'});
            if ~all(validStatus)
                error('BMS:CacheSourceCleanup:PartitionNotClosed', ...
                    'Partition contains a cache record that is not committed.');
            end
            sourceFiles = cellstr(string({records.source_path}));
            selectedPaths = bms.data.JiulongjiangCsvDataSource. ...
                configuredCleanupCsvPaths(csvDir, cfg);
            selectedKeys = lower(string(cellfun(@(p) ...
                bms.data.VerifiedSourceCsvCleanupService.canonicalPath(p), ...
                selectedPaths, 'UniformOutput', false)));
            sourceKeys = lower(string(cellfun(@(p) ...
                bms.data.VerifiedSourceCsvCleanupService.canonicalPath(p), ...
                sourceFiles, 'UniformOutput', false)));
            if isempty(selectedKeys) || ~all(ismember(sourceKeys, selectedKeys))
                error('BMS:CacheSourceCleanup:SourceOutsideConfiguredScope', ...
                    ['Cleanup records contain a CSV that the active bridge runtime ' ...
                     'would not select from this daily partition.']);
            end
            for i = 1:numel(records)
                source = sourceFiles{i};
                sourceInfo = bms.data.CacheManager.buildSourceRecords({source});
                if isempty(sourceInfo) || ~logical(sourceInfo.exists) ...
                        || double(sourceInfo.bytes) ~= double(records(i).source_bytes) ...
                        || ~strcmp(char(string(sourceInfo.modified_at)), ...
                            char(string(records(i).source_modified_at)))
                    error('BMS:CacheSourceCleanup:SourceChanged', ...
                        'CSV changed after cache publication: %s', source);
                end
                if ~bms.data.JiulongjiangCsvDataSource.validateStandaloneRawCache( ...
                        records(i).cache_path, cfg)
                    error('BMS:CacheSourceCleanup:CacheValidationFailed', ...
                        'Cache is not independently loadable: %s', records(i).cache_path);
                end
            end
            archiveProof = bms.data.ArchiveExtractService.verifyRecoveryForFiles( ...
                root, day, cfg, sourceFiles);
            cleanupScope = bms.data.JiulongjiangCsvDataSource. ...
                configuredCleanupSourceScope(cfg);
            receiptId = char(java.util.UUID.randomUUID());
            entries = repmat(bms.data.VerifiedSourceCsvCleanupService.emptyEntry(), ...
                1, numel(records));
            for i = 1:numel(records)
                source = char(string(records(i).source_path));
                cachePath = char(string(records(i).cache_path));
                metadataPath = char(string(records(i).metadata_path));
                [folder, name, ext] = fileparts(source);
                temp = fullfile(folder, sprintf('.%s%s.bmsdelete.%s', ...
                    name, ext, receiptId));
                meta = bms.io.JsonFile.read(metadataPath);
                cacheInfo = dir(cachePath);
                metadataInfo = dir(metadataPath);
                matBytes = bms.data.VerifiedSourceCsvCleanupService. ...
                    fieldDouble(meta, 'mat_bytes');
                if isempty(cacheInfo) || isempty(metadataInfo) ...
                        || double(cacheInfo(1).bytes) ~= matBytes ...
                        || double(cacheInfo(1).bytes) ...
                            + double(metadataInfo(1).bytes) ...
                            ~= double(records(i).cache_bytes)
                    error('BMS:CacheSourceCleanup:CachePairChanged', ...
                        'Cache pair changed after preparation: %s', cachePath);
                end
                entries(i) = struct( ...
                    'source_path', source, ...
                    'temporary_path', temp, ...
                    'source_bytes', double(records(i).source_bytes), ...
                    'source_modified_at', char(string(records(i).source_modified_at)), ...
                    'source_modified_millis', ...
                        bms.data.VerifiedSourceCsvCleanupService. ...
                            fileModifiedMillis(source), ...
                    'cache_path', cachePath, ...
                    'cache_modified_millis', ...
                        bms.data.VerifiedSourceCsvCleanupService. ...
                            fileModifiedMillis(cachePath), ...
                    'metadata_path', metadataPath, ...
                    'metadata_bytes', double(metadataInfo(1).bytes), ...
                    'metadata_modified_millis', ...
                        bms.data.VerifiedSourceCsvCleanupService. ...
                            fileModifiedMillis(metadataPath), ...
                    'cache_bytes', double(records(i).cache_bytes), ...
                    'pair_id', bms.data.VerifiedSourceCsvCleanupService.fieldText(meta, 'pair_id'), ...
                    'mat_bytes', matBytes, ...
                    'state', 'pending', 'renamed_at', '', 'deleted_at', '', ...
                    'error_message', '');
            end
            receipt = struct( ...
                'schema_version', 2, ...
                'receipt_type', 'verified_cache_source_cleanup', ...
                'receipt_id', receiptId, ...
                'status', 'prepared', ...
                'mode', options.mode, ...
                'commit_scope', options.commit_scope, ...
                'recovery_policy', options.recovery_policy, ...
                'confirmation', options.confirmation, ...
                'confirmed_at', options.confirmed_at, ...
                'day', char(string(day)), ...
                'partition_root', partitionRoot, ...
                'csv_dir', char(string(csvDir)), ...
                'cache_version', char(string(cacheVersion)), ...
                'config_hash', char(string(configHash)), ...
                'cleanup_scope_hash', cleanupScope.hash, ...
                'cleanup_scope_count', cleanupScope.count, ...
                'cleanup_scope_ids', {cleanupScope.ids}, ...
                'cleanup_lock_path', cleanupLockPath, ...
                'prepared_at', datestr(datetime('now'), 'yyyy-mm-dd HH:MM:ss'), ...
                'committed_at', '', ...
                'deleted_count', 0, ...
                'deleted_bytes', 0, ...
                'archive_proof', archiveProof, ...
                'files', entries, ...
                'error_identifier', '', ...
                'error_message', '');
            receipt = bms.data.VerifiedSourceCsvCleanupService. ...
                publishCleanupAuthorization( ...
                    receiptPath, receipt, cleanupLockPath);
            receipt = bms.data.VerifiedSourceCsvCleanupService. ...
                publishReceipt(receiptPath, receipt);
            receipt = bms.data.VerifiedSourceCsvCleanupService.commitReceipt( ...
                receiptPath, receipt, cfg, true);
            result.status = receipt.status;
            result.receipt_path = receiptPath;
            result.deleted_count = receipt.deleted_count;
            result.deleted_bytes = receipt.deleted_bytes;
        end
    end

    methods (Static, Access = private)
        function receipt = resumeReceipt(receiptPath, receipt, cfg, cacheVersion, configHash)
            bms.data.VerifiedSourceCsvCleanupService.assertReceiptBinding( ...
                receipt, cacheVersion, configHash, receiptPath, cfg);
            if bms.data.VerifiedSourceCsvCleanupService. ...
                    hasCleanupAuthorization(receipt)
                bms.data.VerifiedSourceCsvCleanupService. ...
                    assertCleanupAuthorization(receipt, receiptPath, ...
                        bms.data.VerifiedSourceCsvCleanupService. ...
                            expectedJljCleanupLockPath(receipt), false);
            end
            if ~bms.data.ArchiveExtractService.verifyArchiveProof(receipt.archive_proof)
                error('BMS:CacheSourceCleanup:RecoveryArchiveChanged', ...
                    'Recovery ZIP changed; interrupted cleanup cannot continue: %s', receiptPath);
            end
            receipt = bms.data.VerifiedSourceCsvCleanupService.commitReceipt( ...
                receiptPath, receipt, cfg);
        end

        function receipt = commitReceipt(receiptPath, receipt, cfg, freshPrepared)
            if nargin < 4, freshPrepared = false; end
            if freshPrepared
                bms.data.VerifiedSourceCsvCleanupService. ...
                    assertFreshPreparedReceipt(receipt, receiptPath);
                if ~bms.data.VerifiedSourceCsvCleanupService. ...
                        hasCleanupAuthorization(receipt) ...
                        || ~isfield(receipt, 'cleanup_lock_path')
                    error('BMS:CacheSourceCleanup:AuthorizationBindingInvalid', ...
                        'Fresh cleanup requires an immutable day authorization.');
                end
                bms.data.VerifiedSourceCsvCleanupService. ...
                    assertCleanupAuthorization(receipt, receiptPath, ...
                        bms.data.VerifiedSourceCsvCleanupService. ...
                            expectedJljCleanupLockPath(receipt), true);
                receipt.commit_validation_mode = ...
                    'batch_authorized_fastpath';
            else
                if bms.data.VerifiedSourceCsvCleanupService. ...
                        hasCleanupAuthorization(receipt)
                    if ~isfield(receipt, 'cleanup_lock_path')
                        error('BMS:CacheSourceCleanup:AuthorizationBindingInvalid', ...
                            'Authorized cleanup receipt has no day-lock path.');
                    end
                    bms.data.VerifiedSourceCsvCleanupService. ...
                        assertCleanupAuthorization(receipt, receiptPath, ...
                            bms.data.VerifiedSourceCsvCleanupService. ...
                                expectedJljCleanupLockPath(receipt), false);
                end
                receipt.commit_validation_mode = 'strict_recovery';
            end
            originalStatus = char(string(receipt.status));
            archivePath = char(string(receipt.archive_proof.archive_path));
            try
                zipGuard = java.util.zip.ZipFile(archivePath); %#ok<NASGU>
                zipGuardCleanup = onCleanup(@() zipGuard.close()); %#ok<NASGU>
            catch ME
                error('BMS:CacheSourceCleanup:RecoveryArchiveLockFailed', ...
                    'Unable to lock the recovery ZIP for cleanup: %s (%s)', ...
                    archivePath, ME.message);
            end
            if ~bms.data.ArchiveExtractService.verifyArchiveProof( ...
                    receipt.archive_proof, ~freshPrepared)
                error('BMS:CacheSourceCleanup:RecoveryArchiveChanged', ...
                    'Recovery ZIP changed before CSV deletion: %s', receiptPath);
            end
            entries = receipt.files;
            renamed = false(1, numel(entries));
            try
                receipt.status = 'renaming';
                receipt = bms.data.VerifiedSourceCsvCleanupService. ...
                    publishReceipt(receiptPath, receipt);
                for i = 1:numel(entries)
                    source = char(string(entries(i).source_path));
                    temp = char(string(entries(i).temporary_path));
                    bms.data.VerifiedSourceCsvCleanupService.assertEntryPaths( ...
                        receipt, entries(i));
                    if freshPrepared
                        bms.data.VerifiedSourceCsvCleanupService. ...
                            assertFreshPreparedCache(entries(i));
                    else
                        bms.data.VerifiedSourceCsvCleanupService.assertCacheEntry( ...
                            receipt, entries(i), cfg);
                    end
                    sourceExists = isfile(source);
                    tempExists = isfile(temp);
                    if sourceExists && tempExists
                        error('BMS:CacheSourceCleanup:DuplicatePendingSource', ...
                            'Both original and pending-delete files exist: %s', source);
                    elseif sourceExists
                        if freshPrepared
                            bms.data.VerifiedSourceCsvCleanupService. ...
                                assertFreshPreparedSource(entries(i), source);
                        else
                            info = bms.data.CacheManager.buildSourceRecords({source});
                            if double(info.bytes) ~= double(entries(i).source_bytes) ...
                                    || ~strcmp(char(string(info.modified_at)), ...
                                        char(string(entries(i).source_modified_at)))
                                error('BMS:CacheSourceCleanup:SourceChanged', ...
                                    'CSV changed before deletion commit: %s', source);
                            end
                            bms.data.VerifiedSourceCsvCleanupService. ...
                                assertCurrentContentMatchesRecovery( ...
                                receipt, entries(i), source, ...
                                'BMS:CacheSourceCleanup:SourceContentChanged');
                        end
                        [ok, message] = movefile(source, temp);
                        if ~ok
                            error('BMS:CacheSourceCleanup:RenameFailed', ...
                                'Unable to stage CSV for deletion: %s (%s)', source, message);
                        end
                        renamed(i) = true;
                        entries(i).state = 'renamed';
                        entries(i).renamed_at = datestr(datetime('now'), 'yyyy-mm-dd HH:MM:ss');
                        if freshPrepared
                            bms.data.VerifiedSourceCsvCleanupService. ...
                                assertFreshPreparedSource(entries(i), temp);
                        else
                            bms.data.VerifiedSourceCsvCleanupService. ...
                                assertCurrentContentMatchesRecovery( ...
                                receipt, entries(i), temp, ...
                                'BMS:CacheSourceCleanup:StagedSourceContentChanged');
                        end
                    elseif tempExists
                        if freshPrepared
                            bms.data.VerifiedSourceCsvCleanupService. ...
                                assertFreshPreparedSource(entries(i), temp);
                        else
                            tempInfo = bms.data.CacheManager.buildSourceRecords({temp});
                            if double(tempInfo.bytes) ~= double(entries(i).source_bytes) ...
                                    || ~strcmp(char(string(tempInfo.modified_at)), ...
                                        char(string(entries(i).source_modified_at)))
                                error('BMS:CacheSourceCleanup:StagedSourceChanged', ...
                                    ['Pending-delete CSV no longer matches the source bound ' ...
                                     'to the cleanup receipt: %s'], temp);
                            end
                            bms.data.VerifiedSourceCsvCleanupService. ...
                                assertCurrentContentMatchesRecovery( ...
                                receipt, entries(i), temp, ...
                                'BMS:CacheSourceCleanup:StagedSourceContentChanged');
                        end
                        renamed(i) = true;
                        entries(i).state = 'renamed_reconciled';
                    else
                        if ~ismember(originalStatus, {'deleting','partial','committed'})
                            error('BMS:CacheSourceCleanup:SourceDisappearedBeforeDelete', ...
                                ['Neither original nor staged CSV exists before the delete ' ...
                                 'phase: %s'], source);
                        end
                        entries(i).state = 'deleted_reconciled';
                        entries(i).deleted_at = datestr(datetime('now'), 'yyyy-mm-dd HH:MM:ss');
                    end
                    receipt.files = entries;
                    if bms.data.VerifiedSourceCsvCleanupService. ...
                            shouldCheckpointReceipt(i, numel(entries))
                        receipt = bms.data.VerifiedSourceCsvCleanupService. ...
                            publishReceipt(receiptPath, receipt);
                    end
                end
            catch ME
                [rollbackOk, rollbackMessage] = ...
                    bms.data.VerifiedSourceCsvCleanupService.rollbackRenamed( ...
                    entries, renamed);
                if rollbackOk
                    receipt.status = 'rename_failed_rolled_back';
                else
                    receipt.status = 'rename_failed_rollback_incomplete';
                end
                receipt.error_identifier = ME.identifier;
                receipt.error_message = strjoin( ...
                    {ME.message, rollbackMessage}, ' | ');
                receipt.files = entries;
                receipt = bms.data.VerifiedSourceCsvCleanupService. ...
                    publishReceipt(receiptPath, receipt);
                if ~rollbackOk
                    error('BMS:CacheSourceCleanup:RollbackFailed', '%s', ...
                        receipt.error_message);
                end
                rethrow(ME);
            end

            % Crash recovery re-proves source bytes and selected ZIP payloads.
            % A fresh transaction instead re-checks its immutable day
            % authorization: the same lock has remained live since the one
            % source-CRC/cache/ZIP acceptance pass that created it.
            archiveVerified = true;
            if freshPrepared
                bms.data.VerifiedSourceCsvCleanupService. ...
                    assertCleanupAuthorization(receipt, receiptPath, ...
                        bms.data.VerifiedSourceCsvCleanupService. ...
                            expectedJljCleanupLockPath(receipt), true);
            else
                archiveVerified = bms.data.ArchiveExtractService. ...
                    verifyArchiveProof(receipt.archive_proof, true);
            end
            if ~archiveVerified
                [rollbackOk, rollbackMessage] = ...
                    bms.data.VerifiedSourceCsvCleanupService.rollbackRenamed( ...
                    entries, renamed);
                if rollbackOk
                    receipt.status = 'recovery_changed_rolled_back';
                else
                    receipt.status = 'recovery_changed_rollback_incomplete';
                end
                receipt.error_identifier = ...
                    'BMS:CacheSourceCleanup:RecoveryArchiveChanged';
                receipt.error_message = strjoin({ ...
                    'Recovery ZIP changed after staging and before deletion.', ...
                    rollbackMessage}, ' | ');
                receipt.files = entries;
                receipt = bms.data.VerifiedSourceCsvCleanupService. ...
                    publishReceipt(receiptPath, receipt);
                if ~rollbackOk
                    error('BMS:CacheSourceCleanup:RollbackFailed', '%s', ...
                        receipt.error_message);
                end
                error(receipt.error_identifier, '%s', receipt.error_message);
            end
            receipt.status = 'deleting';
            receipt.files = entries;
            if freshPrepared
                bms.data.VerifiedSourceCsvCleanupService. ...
                    assertCleanupAuthorization(receipt, receiptPath, ...
                        bms.data.VerifiedSourceCsvCleanupService. ...
                            expectedJljCleanupLockPath(receipt), true);
            end
            receipt = bms.data.VerifiedSourceCsvCleanupService. ...
                publishReceipt(receiptPath, receipt);
            try
                for i = 1:numel(entries)
                    temp = char(string(entries(i).temporary_path));
                    bms.data.VerifiedSourceCsvCleanupService.assertEntryPaths( ...
                        receipt, entries(i));
                    if freshPrepared
                        bms.data.VerifiedSourceCsvCleanupService. ...
                            assertFreshPreparedCache(entries(i));
                        if isfile(temp)
                            bms.data.VerifiedSourceCsvCleanupService. ...
                                assertFreshPreparedSource(entries(i), temp);
                        end
                    else
                        % Cache viability is a precondition for deleting the
                        % only staged source copy, never a post-delete check.
                        bms.data.VerifiedSourceCsvCleanupService.assertCacheEntry( ...
                            receipt, entries(i), cfg);
                    end
                    if isfile(temp)
                        if ~freshPrepared
                            bms.data.VerifiedSourceCsvCleanupService. ...
                                assertCurrentContentMatchesRecovery( ...
                                receipt, entries(i), temp, ...
                                'BMS:CacheSourceCleanup:StagedSourceContentChanged');
                        end
                        delete(temp);
                    end
                    if isfile(char(string(entries(i).source_path))) || isfile(temp)
                        error('BMS:CacheSourceCleanup:DeleteFailed', ...
                            'CSV still exists after deletion attempt: %s', entries(i).source_path);
                    end
                    entries(i).state = 'deleted';
                    if isempty(entries(i).deleted_at)
                        entries(i).deleted_at = datestr(datetime('now'), 'yyyy-mm-dd HH:MM:ss');
                    end
                    receipt.files = entries;
                    receipt.deleted_count = nnz(ismember(cellstr(string({entries.state})), ...
                        {'deleted','deleted_reconciled'}));
                    receipt.deleted_bytes = sum(double([entries(ismember( ...
                        cellstr(string({entries.state})), ...
                        {'deleted','deleted_reconciled'})).source_bytes]));
                    if bms.data.VerifiedSourceCsvCleanupService. ...
                            shouldCheckpointReceipt(i, numel(entries))
                        receipt = bms.data.VerifiedSourceCsvCleanupService. ...
                            publishReceipt(receiptPath, receipt);
                    end
                end
            catch ME
                receipt.status = 'partial';
                receipt.error_identifier = ME.identifier;
                receipt.error_message = ME.message;
                receipt.files = entries;
                receipt = bms.data.VerifiedSourceCsvCleanupService. ...
                    publishReceipt(receiptPath, receipt);
                rethrow(ME);
            end
            receipt.status = 'committed';
            receipt.committed_at = datestr(datetime('now'), 'yyyy-mm-dd HH:MM:ss');
            receipt.error_identifier = '';
            receipt.error_message = '';
            receipt.deleted_count = numel(entries);
            receipt.deleted_bytes = sum(double([entries.source_bytes]));
            receipt.files = entries;
            receipt = bms.data.VerifiedSourceCsvCleanupService. ...
                publishReceipt(receiptPath, receipt);
        end

    end

    methods (Static)

        function interval = receiptCheckpointInterval()
            %RECEIPTCHECKPOINTINTERVAL Bound receipt rewrites for large days.
            interval = 32;
        end

        function tf = shouldCheckpointReceipt(index, total)
            interval = bms.data.VerifiedSourceCsvCleanupService. ...
                receiptCheckpointInterval();
            tf = index == total || mod(index, interval) == 0;
        end

        function receipt = publishReceipt(receiptPath, receipt)
            %PUBLISHRECEIPT Publish atomically and retain a diagnostic count.
            %   The count is not cleanup authorization; it lets acceptance
            %   prove that durable progress is checkpointed in bounded chunks
            %   instead of rewriting the full receipt for every source file.
            if ~isfield(receipt, 'checkpoint_interval')
                receipt.checkpoint_interval = ...
                    bms.data.VerifiedSourceCsvCleanupService. ...
                        receiptCheckpointInterval();
            end
            publishCount = 0;
            if isfield(receipt, 'receipt_publish_count')
                value = receipt.receipt_publish_count;
                if ~isnumeric(value) || ~isscalar(value) ...
                        || ~isfinite(double(value)) || double(value) < 0 ...
                        || double(value) ~= floor(double(value))
                    error('BMS:CacheSourceCleanup:ReceiptPublishCountInvalid', ...
                        'Cleanup receipt publish count is invalid: %s', receiptPath);
                end
                publishCount = double(value);
            end
            receipt.receipt_publish_count = publishCount + 1;
            bms.core.Logger.writeJson(receiptPath, receipt);
        end

        function receipt = publishCleanupAuthorization( ...
                receiptPath, receipt, lockPath)
            %PUBLISHCLEANUPAUTHORIZATION Write one immutable day allowlist.
            %   This document is the fresh transaction's destructive boundary:
            %   source CRCs, cache pair identities, selected ZIP inventories,
            %   configuration and the exact lock owner are frozen together.
            authorizationPath = ...
                bms.data.VerifiedSourceCsvCleanupService. ...
                    cleanupAuthorizationPath(receiptPath, receipt.receipt_id);
            authorization = bms.data.VerifiedSourceCsvCleanupService. ...
                buildCleanupAuthorization( ...
                    receiptPath, receipt, lockPath, authorizationPath);
            authorization.authorization_hash = ...
                bms.data.CacheManager.configHash(authorization);
            if isfile(authorizationPath)
                existing = bms.io.JsonFile.read(authorizationPath);
                if ~isfield(existing, 'authorization_hash') ...
                        || ~strcmp(char(string(existing.authorization_hash)), ...
                            authorization.authorization_hash)
                    error('BMS:CacheSourceCleanup:AuthorizationImmutableConflict', ...
                        ['Cleanup authorization already exists with different ' ...
                         'content: %s'], authorizationPath);
                end
            else
                bms.core.Logger.writeJson(authorizationPath, authorization);
            end
            receipt.authorization_schema_version = 1;
            receipt.authorization_id = char(string(receipt.receipt_id));
            receipt.authorization_path = authorizationPath;
            receipt.authorization_hash = authorization.authorization_hash;
            bms.data.VerifiedSourceCsvCleanupService. ...
                assertCleanupAuthorization( ...
                    receipt, receiptPath, lockPath, true);
        end

        function tf = hasCleanupAuthorization(receipt)
            fields = {'authorization_schema_version','authorization_id', ...
                'authorization_path','authorization_hash'};
            present = isfield(receipt, fields);
            if any(present) && ~all(present)
                error('BMS:CacheSourceCleanup:AuthorizationBindingInvalid', ...
                    'Cleanup receipt has a partial authorization binding.');
            end
            tf = all(present);
            if tf && ~isfield(receipt, 'cleanup_lock_path')
                error('BMS:CacheSourceCleanup:AuthorizationBindingInvalid', ...
                    'Authorized cleanup receipt has no day-lock path.');
            end
        end

        function authorization = assertCleanupAuthorization( ...
                receipt, receiptPath, lockPath, requireOriginalLockOwner)
            if nargin < 4, requireOriginalLockOwner = false; end
            requiredReceipt = {'authorization_schema_version', ...
                'authorization_id','authorization_path','authorization_hash', ...
                'cleanup_lock_path'};
            if ~all(isfield(receipt, requiredReceipt)) ...
                    || double(receipt.authorization_schema_version) ~= 1 ...
                    || ~strcmp(char(string(receipt.authorization_id)), ...
                        char(string(receipt.receipt_id)))
                error('BMS:CacheSourceCleanup:AuthorizationBindingInvalid', ...
                    'Cleanup receipt has no valid immutable authorization binding.');
            end
            expectedPath = bms.data.VerifiedSourceCsvCleanupService. ...
                cleanupAuthorizationPath(receiptPath, receipt.receipt_id);
            authorizationPath = char(string(receipt.authorization_path));
            if ~strcmpi( ...
                    bms.data.VerifiedSourceCsvCleanupService. ...
                        canonicalPath(authorizationPath), ...
                    bms.data.VerifiedSourceCsvCleanupService. ...
                        canonicalPath(expectedPath)) ...
                    || ~isfile(authorizationPath)
                error('BMS:CacheSourceCleanup:AuthorizationBindingInvalid', ...
                    'Cleanup authorization path is missing or unsafe: %s', ...
                    authorizationPath);
            end
            authorization = bms.io.JsonFile.read(authorizationPath);
            required = {'schema_version','authorization_type', ...
                'authorization_id','status','receipt_path','day', ...
                'lock_path','lock_owner','config_binding','file_count', ...
                'source_bytes','cache_bytes','files','archive_inventory', ...
                'authorization_path','authorization_hash'};
            if ~isstruct(authorization) || ~isscalar(authorization) ...
                    || ~all(isfield(authorization, required)) ...
                    || double(authorization.schema_version) ~= 1 ...
                    || ~strcmp(char(string(authorization.authorization_type)), ...
                        'verified_cache_source_cleanup_authorization') ...
                    || ~strcmp(char(string(authorization.status)), 'authorized') ...
                    || ~strcmp(char(string(authorization.authorization_id)), ...
                        char(string(receipt.receipt_id))) ...
                    || ~strcmp(char(string(authorization.day)), ...
                        char(string(receipt.day))) ...
                    || ~strcmpi( ...
                        bms.data.VerifiedSourceCsvCleanupService. ...
                            canonicalPath(authorization.receipt_path), ...
                        bms.data.VerifiedSourceCsvCleanupService. ...
                            canonicalPath(receiptPath)) ...
                    || ~strcmpi( ...
                        bms.data.VerifiedSourceCsvCleanupService. ...
                            canonicalPath(authorization.authorization_path), ...
                        bms.data.VerifiedSourceCsvCleanupService. ...
                            canonicalPath(authorizationPath)) ...
                    || ~strcmpi( ...
                        bms.data.VerifiedSourceCsvCleanupService. ...
                            canonicalPath(receipt.cleanup_lock_path), ...
                        bms.data.VerifiedSourceCsvCleanupService. ...
                            canonicalPath(authorization.lock_path))
                error('BMS:CacheSourceCleanup:AuthorizationInvalid', ...
                    'Cleanup authorization identity is invalid: %s', ...
                    authorizationPath);
            end
            payload = rmfield(authorization, 'authorization_hash');
            actualHash = bms.data.CacheManager.configHash(payload);
            if ~strcmp(actualHash, ...
                    char(string(authorization.authorization_hash))) ...
                    || ~strcmp(actualHash, ...
                        char(string(receipt.authorization_hash)))
                error('BMS:CacheSourceCleanup:AuthorizationHashMismatch', ...
                    'Cleanup authorization content hash changed: %s', ...
                    authorizationPath);
            end
            expectedFiles = bms.data.VerifiedSourceCsvCleanupService. ...
                cleanupAuthorizationFiles(receipt);
            expectedArchives = bms.data.VerifiedSourceCsvCleanupService. ...
                receiptArchiveInventory(receipt);
            expectedConfig = bms.data.VerifiedSourceCsvCleanupService. ...
                cleanupAuthorizationConfig(receipt);
            if double(authorization.file_count) ~= numel(expectedFiles) ...
                    || double(authorization.source_bytes) ...
                        ~= sum(double([expectedFiles.source_bytes])) ...
                    || double(authorization.cache_bytes) ...
                        ~= sum(double([expectedFiles.cache_bytes])) ...
                    || ~strcmp( ...
                        bms.data.CacheManager.configHash(authorization.files), ...
                        bms.data.CacheManager.configHash(expectedFiles)) ...
                    || ~strcmp( ...
                        bms.data.CacheManager.configHash( ...
                            authorization.archive_inventory), ...
                        bms.data.CacheManager.configHash(expectedArchives)) ...
                    || ~strcmp( ...
                        bms.data.CacheManager.configHash( ...
                            authorization.config_binding), ...
                        bms.data.CacheManager.configHash(expectedConfig))
                error('BMS:CacheSourceCleanup:AuthorizationBindingInvalid', ...
                    'Cleanup authorization no longer matches its receipt.');
            end
            lockPath = char(string(lockPath));
            if ~strcmpi( ...
                    bms.data.VerifiedSourceCsvCleanupService. ...
                        canonicalPath(authorization.lock_path), ...
                    bms.data.VerifiedSourceCsvCleanupService. ...
                        canonicalPath(lockPath))
                error('BMS:CacheSourceCleanup:AuthorizationLockMismatch', ...
                    'Cleanup authorization is bound to a different day lock.');
            end
            if logical(requireOriginalLockOwner)
                ownerPath = fullfile(lockPath, 'owner.json');
                if ~isfolder(lockPath) || ~isfile(ownerPath)
                    error('BMS:CacheSourceCleanup:AuthorizationLockMissing', ...
                        'Authorized cleanup day lock is not live: %s', lockPath);
                end
                currentOwner = bms.io.JsonFile.read(ownerPath);
                if ~isfield(currentOwner, 'token') ...
                        || ~isfield(authorization.lock_owner, 'token') ...
                        || ~strcmp(char(string(currentOwner.token)), ...
                            char(string(authorization.lock_owner.token))) ...
                        || ~isfield(currentOwner, 'pid') ...
                        || ~isfield(authorization.lock_owner, 'pid') ...
                        || double(currentOwner.pid) ...
                            ~= double(authorization.lock_owner.pid) ...
                        || double(currentOwner.pid) ...
                            ~= bms.core.DirectoryLeaseLock.currentPid()
                    error('BMS:CacheSourceCleanup:AuthorizationLockOwnerChanged', ...
                        'Cleanup authorization is not held by this process.');
                end
            end
        end

        function millis = fileModifiedMillis(pathValue)
            pathValue = char(string(pathValue));
            if ~isfile(pathValue)
                error('BMS:CacheSourceCleanup:FreshFingerprintMissing', ...
                    'Fresh preparation fingerprint target is missing: %s', ...
                    pathValue);
            end
            millis = double(java.io.File(pathValue).lastModified());
            if ~isfinite(millis) || millis < 0
                error('BMS:CacheSourceCleanup:FreshFingerprintInvalid', ...
                    'Fresh preparation mtime is invalid: %s', pathValue);
            end
        end

        function assertFreshFileFingerprint(pathValue, expectedBytes, ...
                expectedModifiedMillis, identifier)
            pathValue = char(string(pathValue));
            info = dir(pathValue);
            actualModifiedMillis = ...
                bms.data.VerifiedSourceCsvCleanupService. ...
                    fileModifiedMillis(pathValue);
            if isempty(info) || double(info(1).bytes) ~= double(expectedBytes) ...
                    || actualModifiedMillis ~= double(expectedModifiedMillis)
                error(identifier, ...
                    'File path/size/mtime changed after fresh preparation: %s', ...
                    pathValue);
            end
        end

        function validateFreshDeletionEntry(receipt, entry, cfg)
            %VALIDATEFRESHDELETIONENTRY Explicit strong diagnostic verifier.
            %   Normal fresh commits use one immutable natural-day
            %   authorization rather than invoking this full source/cache
            %   content verifier once per CSV. It remains useful for focused
            %   diagnostics and negative tests.
            source = char(string(entry.source_path));
            staged = char(string(entry.temporary_path));
            bms.data.VerifiedSourceCsvCleanupService.assertEntryPaths( ...
                receipt, entry);
            if isfile(source) || ~isfile(staged)
                error('BMS:CacheSourceCleanup:DeleteStateInvalid', ...
                    'Fresh delete gate requires one staged CSV: %s', staged);
            end
            bms.data.VerifiedSourceCsvCleanupService. ...
                assertCurrentContentMatchesRecovery( ...
                    receipt, entry, staged, ...
                    'BMS:CacheSourceCleanup:StagedSourceContentChanged');
            bms.data.VerifiedSourceCsvCleanupService.assertCacheEntry( ...
