        function files = cleanupAuthorizationFiles(receipt)
            if ~isfield(receipt, 'files') || isempty(receipt.files)
                error('BMS:CacheSourceCleanup:AuthorizationBindingInvalid', ...
                    'Cleanup receipt has no files to authorize.');
            end
            archives = bms.data.VerifiedSourceCsvCleanupService. ...
                receiptArchiveInventory(receipt);
            template = struct( ...
                'source_path', '', 'temporary_path', '', ...
                'source_bytes', 0, 'source_crc32', 0, ...
                'source_modified_at', '', 'source_modified_millis', 0, ...
                'cache_path', '', 'cache_modified_millis', 0, ...
                'metadata_path', '', 'metadata_bytes', 0, ...
                'metadata_modified_millis', 0, 'pair_id', '', ...
                'mat_bytes', 0, 'cache_bytes', 0, 'sample_count', 0);
            files = repmat(template, 1, numel(receipt.files));
            for i = 1:numel(receipt.files)
                entry = receipt.files(i);
                source = bms.data.VerifiedSourceCsvCleanupService. ...
                    canonicalPath(entry.source_path);
                matchCount = 0;
                matched = struct();
                for archiveIndex = 1:numel(archives)
                    proofFiles = archives(archiveIndex).files;
                    if isempty(proofFiles), continue; end
                    paths = cellstr(string({proofFiles.path}));
                    indexes = find(strcmpi(cellfun(@(p) ...
                        bms.data.VerifiedSourceCsvCleanupService. ...
                            canonicalPath(p), paths, 'UniformOutput', false), ...
                        source));
                    for proofIndex = indexes(:).'
                        matchCount = matchCount + 1;
                        matched = proofFiles(proofIndex);
                    end
                end
                if matchCount ~= 1 ...
                        || ~isfield(matched, 'bytes') ...
                        || ~isfield(matched, 'crc32') ...
                        || double(matched.bytes) ~= double(entry.source_bytes) ...
                        || ~isfinite(double(matched.crc32))
                    error('BMS:CacheSourceCleanup:AuthorizationBindingInvalid', ...
                        ['Cleanup source is not bound to exactly one CRC-qualified ' ...
                         'archive entry: %s'], source);
                end
                sampleCount = 0;
                if isfield(entry, 'sample_count')
                    sampleCount = double(entry.sample_count);
                end
                files(i) = struct( ...
                    'source_path', source, ...
                    'temporary_path', ...
                        bms.data.VerifiedSourceCsvCleanupService. ...
                            canonicalPath(entry.temporary_path), ...
                    'source_bytes', double(entry.source_bytes), ...
                    'source_crc32', double(matched.crc32), ...
                    'source_modified_at', ...
                        char(string(entry.source_modified_at)), ...
                    'source_modified_millis', ...
                        double(entry.source_modified_millis), ...
                    'cache_path', ...
                        bms.data.VerifiedSourceCsvCleanupService. ...
                            canonicalPath(entry.cache_path), ...
                    'cache_modified_millis', ...
                        double(entry.cache_modified_millis), ...
                    'metadata_path', ...
                        bms.data.VerifiedSourceCsvCleanupService. ...
                            canonicalPath(entry.metadata_path), ...
                    'metadata_bytes', double(entry.metadata_bytes), ...
                    'metadata_modified_millis', ...
                        double(entry.metadata_modified_millis), ...
                    'pair_id', char(string(entry.pair_id)), ...
                    'mat_bytes', double(entry.mat_bytes), ...
                    'cache_bytes', double(entry.cache_bytes), ...
                    'sample_count', sampleCount);
            end
            [~, order] = sort(lower(string({files.source_path})));
            files = files(order);
        end

        function archives = receiptArchiveInventory(receipt)
